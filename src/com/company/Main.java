package com.company;

public class Main {

    public static void main(String[] args) {
        // Comparison Operators
        int alexAge = 18;
        int sergioAge = 20;
        System.out.println(alexAge > sergioAge);
        System.out.println(alexAge < sergioAge);
        System.out.println(alexAge == sergioAge);
        System.out.println(alexAge != sergioAge);
    }
}
